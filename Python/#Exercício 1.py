#Fluxograma 1
x = 0

while x < 5:
    print(x)
    x = x + 1

print("Condição não atendida")

#Fluxograma 2
x = 0

while x < 5:
    x = x + 1
    print(x)

print("Condição não atendida")

## A direfernça é que no fluxograma 2 primeiro soma e depois da print, enquanto o primeiro da print e depois soma, ou seja no primeiro aparece 1, 2, 3, 4 e 5, e no segundos os números 0, 1, 2, 3, 4.